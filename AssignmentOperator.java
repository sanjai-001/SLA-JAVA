package pack;

public class AssignmentOperator {

	public static void main(String[] args) {
		int num=10;
		String name="java";
		System.out.println(num);
		System.out.println(name);
		
		//+=operator
		int a=10;
		a+=10;
		System.out.println(a);
		
		//&&(and) operator >-greater
		System.out.println((5>3) && (8>5));
		
		//&&(and) operator <-lesser
		System.out.println((5<3) && (8<5));
		
		//||(or) operator >-greater
		System.out.println((5<3) || (8>5));
		
		//!(not) operator 
		System.out.println(!(5>3));
		
		
	}

}
