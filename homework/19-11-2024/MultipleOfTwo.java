package pack;
import java.util.*;
public class MultipleOfTwo {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int a=sc.nextInt();
		if(a%2==0) {
			System.out.println("It is a multiple of 2");
		}
		else {
			System.out.println("Not a multiple of 2");
		}
		
	}

}
