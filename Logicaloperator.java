package pack;

public class Logicaloperator {

	public static void main(String[] args) {
		//&&(and) operator >-greater
				System.out.println((5>3) && (8>5));
				
				//&&(and) operator <-lesser
				System.out.println((5<3) && (8<5));
				
				//||(or) operator >-greater
				System.out.println((5<3) || (8>5));
				
				//!(not) operator 
				System.out.println(!(5>3));

	}

}
